using TestProject.Interfaces;
using TestProject.Repositories;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.AddControllers()
                .AddJsonOptions(opt =>
                {
                    opt.JsonSerializerOptions.PropertyNamingPolicy = null;//JsonSerialize methodunu null yaptým. Çünkü Property isimleri yazdýðým þekilde gönderilsin istiyorum.
                });

builder.Services.AddScoped<ITestRepository, TestRepository>();//Dependency Injection çoðu projede sýklýkla kullanýlan bir özellik.

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.MapControllers();
app.UseDefaultFiles();
app.UseStaticFiles();

app.Run();
